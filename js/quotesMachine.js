function assert(condition, sDesc) {
	if (!condition) {
		sDesc = sDesc || 'Assertion failed!';
		throw typeof Error !== 'undefined' ? new Error(sDesc) : sDesc;
	}
}

String.prototype.multiReplace = function(arrSyntax) {
	assert(Array.isArray(arrSyntax), 'String.multiReplace: Invalid \'arrSyntax\'!');
	var sRtn = this;
	$.each(arrSyntax, function(i, syntax) {
		sRtn = sRtn.replace(syntax[0], syntax[1]);
	});
	return sRtn;
};

function getQuote() {
	$('#quotes').animate(
		{
			opacity: 0,
		},
		400,
		function() {
			sMaxim = arrMaxim[Math.floor(Math.random() * arrMaxim.length)];
			quoteArr = sMaxim.split('@');
			quote = quoteArr[0].multiReplace(arrPunctMap);
			if (quoteArr[1]) {
				author = quoteArr[1];
			} else {
				author = 'Anonymous';
			}

			$(this).animate(
				{
					opacity: 1,
				},
				800,
			);
			$('#quote').html(`${quote}&#8212;&#8212;`);
			$('#author').html(author);
		},
	);
}
$(document).ready(function() {
	getQuote();
});

